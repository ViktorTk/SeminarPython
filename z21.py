# Напишите программу для печати всех уникальных
# значений в словаре.

# Input: [{"V": "S001"}, {"V": "S002"}, {"VI": "S001"},
# {"VI": "S005"}, {"VII": "S005"}, {"V": "S009"}, {"VII":
# "S007"}]

# Output: {'S005', 'S002', 'S007', 'S001', 'S009'}


dict = [{"V": "S001"}, {"V": "S002"}, {"VI": "S001"}, {"VI": "S005"}, {"VII": "S005"}, {"V": "S009"}, {"VII": "S007"}]

col = []
for i in dict:
    col = col + list(i.values())

print(col)
print(list(set(col)))









